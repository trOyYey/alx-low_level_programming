#include "3-calc.h"

/**
 * op_add - adds two int
 * @a: int
 * @b: int
 * Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts two integers
 * @a: the first int
 * @b: the second int
 * Return: the difference
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_div - divides two int
 * @a: int
 * @b: int
 * Return: result
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulu of two int
 * @a: int
 * @b: int
 * Return: result
 */

int op_mod(int a, int b)
{
	return (a % b);
}

/**
 * op_mul - multiple of two int
 * @a: int
 * @b: int
 * Return: result
 */

int op_mul(int a, int b)
{
	return (a * b);
}
